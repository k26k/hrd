package loopExample;

public class CharSetEx {
	public static void main(String[] args) {
		char ch =' ';
		for(ch=65;ch<123;ch++) {
			System.out.print(ch+" ");
		}
		System.out.println();
		
		int num=6;
		for(int i=1;i<=9;i++) {
			System.out.println(num+"x"+i+"="+num*i);
		}
		System.out.println();
		
	}
}
